    private void btAdicionarQuantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarQuantidadeActionPerformed
        // TODO add your handling code here:
        if (txtAddEstoque.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo adicionar estoque em branco!");
        } else {
            int quantidade = Integer.parseInt(txtQuantidade.getText());
            int quantidade2 = Integer.parseInt(txtAddEstoque.getText());
            int resultado = quantidade + quantidade2;

            Produtos produto = new Produtos();
            txtQuantidade.setText(Integer.toString(resultado));
            produto.setCodigoProduto(Integer.parseInt(txtCodigo.getText()));
            produto.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            ProdutoDAO dao = new ProdutoDAO();
            dao.incluirQuantidade(produto);

            tabelaConsultaEstoque.setModel(new TabelaModeloProduto(new ProdutoDAO().listarProdutos()));
            txtAddEstoque.setText("");

            jTabbedConsultaEstoque.setSelectedIndex(0);
        }

    }//GEN-LAST:event_btAdicionarQuantidadeActionPerformed

