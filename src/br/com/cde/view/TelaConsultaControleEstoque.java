    private void btRetirarQuantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRetirarQuantidadeActionPerformed
        // TODO add your handling code here:
        if (txtRetirarEstoque.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo retirar estoque em branco!");
        } else {
            int quantidade = Integer.parseInt(txtQuantidade.getText());
            int quantidade2 = Integer.parseInt(txtRetirarEstoque.getText());
            int resultado = quantidade - quantidade2;

            Produtos produto = new Produtos();

            if (resultado == 0) {
                txtRetirarEstoque.setText("0");
                produto.setCodigoProduto(Integer.parseInt(txtCodigo.getText()));
                produto.setQuantidade(Integer.parseInt(txtRetirarEstoque.getText()));
                ProdutoDAO dao = new ProdutoDAO();
                dao.decrementarQuantidade(produto);

                BaixaEstoque baixaEstoque = new BaixaEstoque();
                baixaEstoque.setNome(txtNomeProduto.getText());
                baixaEstoque.setDescricao(txtDescricao.getText());
                baixaEstoque.setTamanho(comboTamanho.getSelectedItem().toString());
                baixaEstoque.setValor(Double.parseDouble(txtValor.getText()));
                baixaEstoque.setLote(comboLote.getSelectedItem().toString());
                baixaEstoque.setTipo(comboTipo.getSelectedItem().toString());
                baixaEstoque.setCategoria(comboCategoria.getSelectedItem().toString());

                BaixaEstoqueDAO bDao = new BaixaEstoqueDAO();
                bDao.cadastrarProdutoBaixa(baixaEstoque);

                produto.setCodigoProduto(Integer.parseInt(txtCodigo.getText()));
                dao.excluirProdutoDeBaixa(produto);
                
                tabelaConsultaEstoque.setModel(new TabelaModeloProduto(new ProdutoDAO().listarProdutos()));

                utilitarios.limpaTela(jPanel2);
                
            } else if (resultado < 0) {
                JOptionPane.showMessageDialog(null, "Não é possivel retirar do estoque a quantidade estipulada");

            } else {
                txtRetirarEstoque.setText(Integer.toString(resultado));
                produto.setCodigoProduto(Integer.parseInt(txtCodigo.getText()));
                produto.setQuantidade(Integer.parseInt(txtRetirarEstoque.getText()));
                ProdutoDAO dao = new ProdutoDAO();
                dao.decrementarQuantidade(produto);
            }

            tabelaConsultaEstoque.setModel(new TabelaModeloProduto(new ProdutoDAO().listarProdutos()));
            txtRetirarEstoque.setText("");

            jTabbedConsultaEstoque.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btRetirarQuantidadeActionPerformed

