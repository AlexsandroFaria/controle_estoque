            st.execute("insert into tabela_tipo (tipo_tipo, descricao_tipo) values ('Utensílios','Utensilios')");

            st.execute("CREATE TABLE tabela_tamanho (\n"
                    + "  codigo_tamanho int(11) NOT NULL AUTO_INCREMENT,\n"
                    + "  tamanho_tamanho varchar(45) DEFAULT NULL,\n"
                    + "  descricao_tamanho varchar(45) DEFAULT NULL,\n"
                    + "  PRIMARY KEY (codigo_tamanho)\n"
                    + ")");

