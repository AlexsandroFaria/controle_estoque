            st.execute("insert into tabela_tamanho (tamanho_tamanho, descricao_tamanho) values ('G','Tamanho Grande');");

            st.execute("CREATE TABLE tabela_produto (\n"
                    + "  codigo_produto int(11) NOT NULL AUTO_INCREMENT,\n"
                    + "  nome_produto varchar(45) DEFAULT NULL,\n"
                    + "  descricao_produto varchar(45) DEFAULT NULL,\n"
                    + "  tamanho_produto varchar(45) DEFAULT NULL,\n"
                    + "  valor_produto double DEFAULT NULL,\n"
                    + "  lote_produto varchar(45) DEFAULT NULL,\n"
                    + "  tipo_produto varchar(45) DEFAULT NULL,\n"
                    + "  categoria_produto varchar(45) DEFAULT NULL,\n"
                    + "  quantidade_produto int(11) DEFAULT NULL,\n"
                    + "  PRIMARY KEY (codigo_produto)\n"
                    + ")");

            st.execute("CREATE TABLE tabela_lote (\n"
                    + "  codigo_lote int(11) NOT NULL AUTO_INCREMENT,\n"
                    + "  lote_lote varchar(45) DEFAULT NULL,\n"
                    + "  descricao_lote varchar(45) DEFAULT NULL,\n"
                    + "  PRIMARY KEY (codigo_lote)\n"
                    + ")");

            st.execute("insert into tabela_Lote (lote_lote, descricao_lote) values ('Lote 1','Primeiro Lote')");

            st.execute("CREATE TABLE tabela_categoria (\n"
                    + "  codigo_categoria int(11) NOT NULL AUTO_INCREMENT,\n"
                    + "  categoria_categoria varchar(45) DEFAULT NULL,\n"
                    + "  descricao_categoria varchar(45) DEFAULT NULL,\n"
                    + "  PRIMARY KEY (codigo_categoria)\n"
                    + ")");

