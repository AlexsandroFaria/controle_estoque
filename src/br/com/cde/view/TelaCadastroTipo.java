    private void tabelaTipoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaTipoMouseClicked
        // TODO add your handling code here:
        painelTamanho.setSelectedIndex(0);
        txtCodigo.setText(tabelaTipo.getValueAt(tabelaTipo.getSelectedRow(), TabelaModeloTipo.COLUNA_CODIGO_TIPO).toString());
        txtTipo.setText(tabelaTipo.getValueAt(tabelaTipo.getSelectedRow(), TabelaModeloTipo.COLUNA_TAMANHO_TIPO).toString());
        txtDescricao.setText(tabelaTipo.getValueAt(tabelaTipo.getSelectedRow(), TabelaModeloTipo.COLUNA_DESCRICAO_TIPO).toString());
        btCadastrar.setEnabled(false);
        btExcluir.setEnabled(true);
    }//GEN-LAST:event_tabelaTipoMouseClicked

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        tipo.setCodigoTipo(Integer.parseInt(txtCodigo.getText()));
        dao.excluirTipo(tipo);

        utilitarios.limpaTela(jPanel2);

        btExcluir.setEnabled(false);
        btCadastrar.setEnabled(true);

        tabelaTipo.setModel(new TabelaModeloTipo(new TipoDAO().listarTipo()));
    }//GEN-LAST:event_btExcluirActionPerformed

