    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        if (txtTipo.getText().isEmpty() || txtDescricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Existem campos em branco!");
        } else {

            tipo.setTipo(txtTipo.getText());
            tipo.setDescricao(txtDescricao.getText());

            dao.cadastrarTipo(tipo);
            
            tabelaTipo.setModel(new TabelaModeloTipo(new TipoDAO().listarTipo()));
        }
        utilitarios.limpaTela(jPanel2);
    }//GEN-LAST:event_btCadastrarActionPerformed

    private void btNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNovoActionPerformed
        // TODO add your handling code here:
        utilitarios.limpaTela(jPanel2);
        btCadastrar.setEnabled(true);
        btExcluir.setEnabled(false);
    }//GEN-LAST:event_btNovoActionPerformed

