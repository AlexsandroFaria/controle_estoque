package br.com.cde.model;

/**
 *
 * @author alafaria
 */
public class BaixaEstoque {
    
    private int codigoBaixaProduto;
    private String nome;
    private String descricao;
    private String tamanho;
    private double valor;
    private String lote;
    private String tipo;
    private String categoria;
    private String data;

    public int getCodigoBaixaProduto() {
        return codigoBaixaProduto;
    }

    public void setCodigoBaixaProduto(int codigoBaixaProduto) {
        this.codigoBaixaProduto = codigoBaixaProduto;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getTamanho() {
        return tamanho;
    }

    public void setTamanho(String tamanho) {
        this.tamanho = tamanho;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public String getLote() {
        return lote;
    }

    public void setLote(String lote) {
        this.lote = lote;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
    
    
}
